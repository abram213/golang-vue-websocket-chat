// ServeWs handles websocket requests from the peer.
func (hub *Hub) ServeWs(app *App, w http.ResponseWriter, r *http.Request) {
	conn, err := upgrader.Upgrade(w, r, nil)
	if err != nil {
		log.Println(err)
		return
	}
	id, ok := r.URL.Query()["id"]
	if !ok || len(id) == 0 {
		fmt.Println("url param 'id' is missing")
	}

	client := &Client{ChatID: id[0], hub: hub, conn: conn, send: make(chan model.Message)}
	client.hub.register <- client

	// Allow collection of memory referenced by the caller by doing all work in
	// new goroutines.
	go client.writePump()
	go client.readPump(app)
}
